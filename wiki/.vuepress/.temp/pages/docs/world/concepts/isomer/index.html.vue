<h2 id="关于「异常构造」的详细定义" tabindex="-1"><a class="header-anchor" href="#关于「异常构造」的详细定义"><span>关于「异常构造」的详细定义</span></a></h2>
<p>在这个世界，即便存在大量离奇事物，【<RouteLink to="/docs/world/concepts/isomer/" class="auto-link">异常构造</RouteLink>】仍是其中最特殊且最危险的一类。</p>
<h3 id="旧定义" tabindex="-1"><a class="header-anchor" href="#旧定义"><span>旧定义</span></a></h3>
<p>在危机管理机构时期，最初的定义是：</p>
<blockquote>
<p>“以极不合理的机制运行的事物。”</p>
</blockquote>
<p>问题在于，这个定义过于宽泛，导致许多“只是难以理解但不算异常”的现象也被划入该类。</p>
<h3 id="新定义" tabindex="-1"><a class="header-anchor" href="#新定义"><span>新定义</span></a></h3>
<p>某位博士提出了一个颠覆性的理论：【<RouteLink to="/docs/world/concepts/isomer/" class="auto-link">异常构造</RouteLink>】的本质，是对【宇宙本源】的消耗行为。<br>
关于【宇宙本源】的性质，他给了一个可理解的类比：</p>
<blockquote>
<p>类似宇宙大爆炸后残余且支持宇宙运行的那部分巨大能量。</p>
</blockquote>
<p>它驱动宇宙膨胀、维持分子运动，但终究有限。当它耗尽，宇宙将趋于热寂甚至开始收缩。<br>
而【异常构造】之所以能够看起来“凭空创造物质、能量”或“违反物理定律”，其根本原因不是无中生有，而是直接消耗宇宙本源来弥补所需能量。</p>
<h3 id="例子" tabindex="-1"><a class="header-anchor" href="#例子"><span>例子</span></a></h3>
<p>例如，一个能“凭空产出能源”的异常构造：它可能在内部维持一个虫洞，连接“一秒之后的未来”，从未来截取能量带回现在。这看似提供无尽能源，但维持虫洞本身、时间能量的跨越，都必须消耗宇宙本源。<br>
此理论让人类第一次意识到：滥用异常构造会直接加速宇宙死亡。异常构造属于全宇宙级别的风险。<br>
危机管理机构因此开始正视异常构造，最终推动了异常管理局的成立。<br>
【异常构造】以编号称呼。</p>
<h2 id="异常构造的分类" tabindex="-1"><a class="header-anchor" href="#异常构造的分类"><span>异常构造的分类</span></a></h2>
<p>异常构造的编号分为两部分，“类型”和“序号”。<br>
异常构造的数量极其稀少，直到2030年为止，确定的异常构造仅有数百余件。因此异常构造以发现顺序来编写序号。<br>
类型稍微复杂一点，异常管理局将异常构造按照危险性、规模、利用难易度、异常值、状态，用不同的字母简称进行排序，用以直观的表示异常构造的简要信息。</p>
<h3 id="规模" tabindex="-1"><a class="header-anchor" href="#规模"><span>规模</span></a></h3>
<p>规模指的是异常构造的大小，分为单人级、地区级、大陆级、星球级、外级。<br>
单人级指的是不超过一栋七层居民楼的大小。<br>
地区级指的是不超过一个国家的大小。<br>
大陆级指的是不超过地球表面的大小。<br>
星球级指的是不超过太阳大小。<br>
外级指的是规模无法计算，无法以大小来形容。</p>
<h3 id="危险性" tabindex="-1"><a class="header-anchor" href="#危险性"><span>危险性</span></a></h3>
<p>危险性是一个综合指标，包含其对人类社会造成的破坏程度、活跃程度等，分为常级，大事件级，国际级，灭世级。<br>
常级，凡人也可以解决的等级。<br>
大事件级，需要多人合作才能解决。<br>
国际级，需要世界各地区人民联合起来才能解决。<br>
灭世级，无法解决，需要穷尽一切，才能抓住一线希望。</p>
<h3 id="利用难易度" tabindex="-1"><a class="header-anchor" href="#利用难易度"><span>利用难易度</span></a></h3>
<p>利用难易度指异常构造是否拿着就能用。<br>
常级，任何人都可以使用。<br>
贵级，需要大量财力。<br>
超凡级，需要特殊能量才可以使用。<br>
社会级，需要多人主持。</p>
